                    Plus, cats are sensitive to the taste and presentation of water. Most cats instinctively gravitate toward fresh, moving water.<br>  

                    Conditions that make cats more prone to dehydration include chronic kidney disease and other conditions, such as diarrhea or diabetes. <br> 


